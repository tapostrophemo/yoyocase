<h2>Help</h2>

<h3>Navigatig the site</h3>
<p>The top bar of the page has useful navigation links. Whenver you're logged in, clicking the link
 in the upper left takes you to your main account page, where you will see links to other pages
 within the site.</p>

<h3>Adding yo-yos</h3>
<p>Go to the <a href="/yoyos">collection</a> page and follow the instructions.</p>

<h3>Adding photos</h3>
<p>You may wonder how to upload photos of your yoyos; <tt>yoyocase.net</tt> currently does not allow
 photo uploads, but you can link photos from several popular photo-hosting sites.</p>
<p>To make your photos available:</p>
<ol>
 <li>upload photos to your <a href="http://www.flickr.com" target="_blank">flickr</a> and/or
  your <a href="http://www.photobucket.com" target="_blank">Photobucket</a> account</li>
 <li>go to the <tt>yoyocase.net</tt> <a href="/preferences">account preferences</a> page and
  follow the instructions there for linking to your flickr/Photobucket account(s).</li>
</ol>
<p>Once those are complete, anytime you add to or edit your collection, <tt>yoyocase.net</tt>
 will retrieve a list and allow you to choose one or more photos.</p>


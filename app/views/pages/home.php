<h2>Welcome to yoyocase.net</h2>

<p>what is yoyocase.net? it is a website that will let you manage your yoyo collection online.
 managing your collection online means you'll be able to more easily share your collection
 with friends, traders, and other yoyo enthusiasts.</p>

<br/>

<p>the site is new and growing, but already you can:</p>
<ul>
 <li>link pictures of your collection from flickr</li>
 <li>post yoyo details (manufacturer, model name, description, etc.)</li>
</ul>

<br/>

<p>some of the features planned for yoyocase.net include:</p>
<ul>
 <li>link pictures from other popular image hosting sites (photobucket, imageshack, imgur, etc.)</li>
 <li>include advanced details (cost, estimated value, condition, etc.)</li>
 <li>keep track of gifts, purchases and trades (date, people/stores involved, etc.)</li>
 <li>view other users' galleries and keep in touch with other yoyo enthusiasts</li>
</ul>

<br/>

<p><a href="/tour">take a tour</a> to see some of the features.</p>

<br/>

<p><small>Note: this site is focused only on yo-yos and yo-yo related items. Posting of illegal or
 inappropriate material can and may be removed by the site operators.</small></p>

